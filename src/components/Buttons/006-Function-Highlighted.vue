<h3>Function - Highlighted</h3>
<p>This special variation of the function button is used for unique actions, or to stand out amongst a group of other function buttons.</p>

```js
<b-button variant="outline-secondary" class="btnFunction btnFunctionHighlighted">SAMPLE BUTTON</b-button>

<b-button variant="outline-secondary" class="btnFunction btnFunctionHighlighted"><i class="fal fa-file-alt fa-lg btnIconLeft"></i>UPLOAD FILE</b-button>

<b-button variant="outline-secondary" class="btnFunction btnFunctionHighlightedPressed"><i class="fal fa-file-alt fa-lg btnIconLeft"></i>PRESSED</b-button>

<b-button variant="outline-secondary" class="btnFunction btnFunctionHighlightedPressed"><span class="spinner-grow spinner-grow-sm btnIconLeft" role="status" aria-hidden="true"></span>LOADING . . .</b-button>
```

