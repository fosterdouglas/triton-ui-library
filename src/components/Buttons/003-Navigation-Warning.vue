<h3>Navigation - Warning</h3>
<p>Generally, this variation should only be used to warn the user that the action will be permanent (as in deleting something), or that it will cancel an operation which will result in the loss of inputed data.</p>

```js
<b-button variant="outline-warningfade" class="btnNavigation btnWarning">SAMPLE BUTTON</b-button>

<b-button variant="outline-warningfade" class="btnNavigation btnWarning">CANCEL<i class="far fa-ban btnIconRight"></i></b-button>

<b-button variant="outline-warningfade" class="btnNavigation btnWarningPressed">PRESSED</b-button>
```

